console.log(`---increment/decrement---`);

//prefiksinis inkrementavimas:

let x1 = 5;
let z1 = ++x1;
console.log(`x1: ${x1}, z1: ${z1}`);
//postfiksinis inkrementavimas:

let x2 = 5;
let z2 = x2++;
console.log(`x2: ${x2}, z2: ${z2}`);

//prefiksinis dekrementavimas:
let x3 = 5;
let z3 = --x3;
console.log(`x3: ${x3}, z3: ${z3}`);

//postfiksinis dekrementavimas:
let x4 = 5;
let z4 = x4--;
console.log(`x4: ${x4}, z4: ${z4}`);